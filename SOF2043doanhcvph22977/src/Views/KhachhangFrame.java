    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
        int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không", "Thông báo !", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            String mess = khachHangService.update(lbl_id.getText(), getForm());
            loadData();
            JOptionPane.showMessageDialog(this, mess);
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn từ chối sửa");
        }
    }//GEN-LAST:event_btn_UpdateActionPerformed

