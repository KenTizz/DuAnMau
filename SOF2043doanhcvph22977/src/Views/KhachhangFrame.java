    private void tb_DanhsachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_DanhsachMouseClicked
        // TODO add your handling code here:
        int row = tb_Danhsach.getSelectedRow();
        tb_Danhsach.setRowSelectionAllowed(true);
        QLKhachHang kh = khachHangService.getOne(tb_Danhsach.getValueAt(row, 1).toString());
        txt_Ma.setText(kh.getMa());
        lbl_id.setText(kh.getId());
        txt_Ho.setText(kh.getHo());
        txt_TenDem.setText(kh.getTenDem());
        txt_Ten.setText(kh.getTen());
        txt_NgaySinh.setDate(kh.getNgaySinh());
        txt_Sdt.setText(kh.getsDT());
        txt_DiaChi.setText(kh.getDiaChi());
        txt_ThanhPho.setText(kh.getThanhPho());
        txt_QuocGia.setText(kh.getQuocGia());
        txt_MatKhau.setText(kh.getMatKhau());
        
    }//GEN-LAST:event_tb_DanhsachMouseClicked


