    public void loadData() {
        tableModel = (DefaultTableModel) tb_Danhsach.getModel();
        tableModel.setRowCount(0);
        for (QLKhachHang qlkh : khachHangService.getListKhach()) {
            tableModel.addRow(qlkh.toDataRow());
        }
    }

    public QLKhachHang getForm() {
        QLKhachHang kh = new QLKhachHang();
        kh.setMa(txt_Ma.getText());
        kh.setHo(txt_Ho.getText());
        kh.setTenDem(txt_TenDem.getText());
        kh.setTen(txt_Ten.getText());
        kh.setNgaySinh(txt_NgaySinh.getDate());
        kh.setsDT(txt_Sdt.getText());
        kh.setDiaChi(txt_DiaChi.getText());
        kh.setThanhPho(txt_ThanhPho.getText());
        kh.setQuocGia(txt_QuocGia.getText());
        kh.setMatKhau(txt_MatKhau.getText());
        return kh;
    }

