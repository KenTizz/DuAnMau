// check//
    public boolean check() {
        if (txt_maChucVu.getText().trim().isEmpty() || txt_tenChucVu.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return false;
        }
        if (!txt_tenChucVu.getText().matches("(\\S+ )*\\S+")) {
            JOptionPane.showMessageDialog(this, "khongh gop le");
            return false;
        }
        return true;
    }
