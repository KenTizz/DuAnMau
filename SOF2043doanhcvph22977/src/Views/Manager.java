    public void loadTableNhanVien() {
        model = (DefaultTableModel) tb_NhanVien.getModel();
        model.setRowCount(0);
        int i = 1;
        for (QLNhanVien nv : nhanVienService.getListNhanVien()) {
            model.addRow(new Object[]{
                i++, nv.getId(), nv.getMa(), nv.hoVaTen(),
                nv.getGioiTinh(),
                nv.getNgaySinh(), nv.getDiaChi(), nv.getsDT(), nv.getTenChucVu(), nv.getTenCuaHang(), nv.getMatKhau(),
                nv.getTrangThai() == 1 ? "Đang Làm" : "Nghỉ Làm"
            });
        }
    }

// getFrom NhanVien
    private NhanVien getFromNhanVien() {
        NhanVien nv = new NhanVien();
        nv.setMa(txt_Ma_NhanVien.getText());
        nv.setHo(txt_Ho_NhanVien.getText());
        nv.setTenDem(txt_TenDem_NhanVien.getText());
        nv.setTen(txt_Ten_NhanVien.getText());
        if (rd_Nam_NhanVien.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }
        nv.setNgaySinh(new Date(txt_NgaySinh_NhanVien.getText()));
        nv.setDiaChi(txt_DiaChi_NhanVien.getText());
        nv.setsDT(txt_SDT_NhanVien.getText());
        nv.setIdChucVu(((QLChucVu) comBoChucVu.getSelectedItem()).getId());
        nv.setIdCuaHang(((QLCuaHang) comBoCuaHang.getSelectedItem()).getId());
        nv.setMatKhau(txt_MatKhau_NhanVien.getText());
        if (rd_DangLam_NhanVien.isSelected()) {
            nv.setTrangThai(1);
        } else {
            nv.setTrangThai(0);
        }
        return nv;
    }
