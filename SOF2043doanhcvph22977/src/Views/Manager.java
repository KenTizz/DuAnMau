    private void loadTableChiTietSP() {
        model = (DefaultTableModel) tb_ChiTietSP.getModel();
        model.setRowCount(0);
        int i = 1;
        for (QLChiTietSP ct : chiTietSPService.getListChiTietSP()) {
            model.addRow(new Object[]{
                i++, ct.getId(), ct.getTenSanPham(), ct.getTenNSX(), ct.getTenDongSP(), ct.getTenMauSac(), ct.getNamBH(), ct.getSoLuongTon(), ct.getGiaNhap(), ct.getGiaBan(), ct.getMoTa()
            });
        }
    }
//    get From Chi Tiet San Pham

    public ChiTietSP getFormChiTietSP() {
        ChiTietSP ct = new ChiTietSP();
        ct.setIdSanPham(((QLSanPham) comBoSP.getSelectedItem()).getId());
        ct.setIdNSX(((QLNhaSanXuat) comBoNSX.getSelectedItem()).getId());
        ct.setIdDongSP(((QLDongSanPham) comBoDongSP.getSelectedItem()).getId());
        ct.setIdMauSac(((QLMauSac) comBoMauSac.getSelectedItem()).getId());
        ct.setNamBH(Integer.parseInt(txt_NamBH.getText()));
        ct.setSoLuongTon(Integer.parseInt(sp_SoLuongTon.getValue().toString()));
        ct.setGiaNhap(BigDecimal.valueOf(Double.parseDouble(txt_GiaNhap.getText())));
        ct.setGiaBan(BigDecimal.valueOf(Double.parseDouble(txt_GiaBan.getText())));
        ct.setMoTa(txt_MoTa.getText());
        return ct;
    }
