    public void getSearch() {
        model = (DefaultTableModel) tb_DanhSach.getModel();
        model.setRowCount(0);
        List<QLChucVu> list = chucVuService.getAllChucVu();
        for (QLChucVu qLChucVu : list) {
            if (qLChucVu.getMa().toLowerCase().contains(txt_Search.getText().trim().toLowerCase())) {
                model.addRow(qLChucVu.toDataRow());
            }
        }

    }

    public void getSearch1() {
        model = (DefaultTableModel) tb_DanhSach.getModel();
        model.setRowCount(0);
        List<QLChucVu> list = chucVuService.getAllChucVu();
        for (QLChucVu qLChucVu : list) {
            if (qLChucVu.getTen().toLowerCase().contains(txt_Search.getText().trim().toLowerCase())) {
                model.addRow(qLChucVu.toDataRow());
            }
        }

    }

    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
        // TODO add your handling code here:
        if (txt_Search.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống");
            return;
        }
        if (cb_search.getSelectedItem().equals("Mã")) {
            if (chucVuService.getOne(txt_Search.getText()) == null) {
                JOptionPane.showMessageDialog(this, "Không có");
                return;
            }
            getSearch();
        } else if (cb_search.getSelectedItem().equals("Tên")) {
            getSearch1();

        }

    }//GEN-LAST:event_btn_SearchActionPerformed


