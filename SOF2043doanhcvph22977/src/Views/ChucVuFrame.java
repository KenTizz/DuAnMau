    public void loadData() {
        model = (DefaultTableModel) tb_DanhSach.getModel();
        model.setRowCount(0);
        for (QLChucVu cv : chucVuService.getAllChucVu()) {
            model.addRow(cv.toDataRow());
        }
    }

    public QLChucVu getFrom() {
        QLChucVu qlcv = new QLChucVu();
        qlcv.setMa(txt_Ma.getText());
        qlcv.setTen(txt_Ten.getText());
        return qlcv;
    }

    public void clearFrom() {
        tb_DanhSach.setRowSelectionAllowed(false);
        lbl_id.setText("");
        txt_Ma.setText("");
        txt_Ten.setText("");
        txt_Search.setText("");
        loadData();
    }

