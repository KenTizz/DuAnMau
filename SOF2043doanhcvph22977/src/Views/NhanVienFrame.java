    private void tb_DanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_DanhSachMouseClicked
        // TODO add your handling code here:
        int row = tb_DanhSach.getSelectedRow();
        tb_DanhSach.setRowSelectionAllowed(true);
        QLNhanVien nv = nhanVienService.getOne((String) tb_DanhSach.getValueAt(row, 1));
        lbl_id.setText(nv.getId());
        txt_Ma.setText(nv.getMa());
        txt_Ho.setText(nv.getHo());
        txt_TenDem.setText(nv.getTenDem());
        txt_Ten.setText(nv.getTen());
        if (nv.getGioiTinh().equals("Nam")) {
            rd_Nam.setSelected(true);
        } else {
            rd_Nu.setSelected(true);
        }
        txt_NgaySinh.setDate(nv.getNgaySinh());
        txt_DiaChi.setText(nv.getDiaChi());
        txt_SDT.setText(nv.getsDT());
        cb_ChucVu.setSelectedItem(tb_DanhSach.getValueAt(row,7));
        cb_CuaHang.setSelectedItem(tb_DanhSach.getValueAt(row,8));
        txt_MatKhau.setText(nv.getMatKhau());
        if (tb_DanhSach.getValueAt(row, 10).toString().equals("Đang Làm")) {
            rd_DangLam.setSelected(true);
        } else {
            rd_NghiLam.setSelected(true);
        }
    }//GEN-LAST:event_tb_DanhSachMouseClicked


