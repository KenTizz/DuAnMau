    public void loadData() {
        tableModel = (DefaultTableModel) tb_DanhSach.getModel();
        tableModel.setRowCount(0);
        for (QLNhanVien qlnv : nhanVienService.getListNhanVien()) {
            tableModel.addRow(qlnv.toDataRow());
        }
    }

    public void loadCBChucVu() {
        comboBoxModel = (DefaultComboBoxModel) cb_ChucVu.getModel();
        for (QLChucVu cv : chucVuService.getAllChucVu()) {
            comboBoxModel.addElement(cv.getTen());
        }

    }

    public void loadCBCuaHang() {
        comboBoxModel = (DefaultComboBoxModel) cb_CuaHang.getModel();
        for (QLCuaHang ch : cuaHangService.getAllCuaHang()) {
            comboBoxModel.addElement(ch.getTen());
        }

    }

    public QLNhanVien getForm() {
        QLNhanVien nv = new QLNhanVien();
        nv.setMa(txt_Ma.getText());
        nv.setHo(txt_Ho.getText());
        nv.setTenDem(txt_TenDem.getText());
        nv.setTen(txt_Ten.getText());
        if (rd_Nam.isSelected()) {
            nv.setGioiTinh("Nam");
        } else {
            nv.setGioiTinh("Nữ");
        }
        nv.setNgaySinh(txt_NgaySinh.getDate());
        nv.setDiaChi(txt_DiaChi.getText());
        nv.setsDT(txt_SDT.getText());
        int idChuVu = cb_ChucVu.getSelectedIndex();
        QLChucVu cv = listChucVu.get(idChuVu);
        nv.setTenChucVu(cv.getId());
        int idCuaHang = cb_CuaHang.getSelectedIndex();
        QLCuaHang ch = listCuaHang.get(idCuaHang);
        nv.setTenCuaHang(ch.getId());
        nv.setMatKhau(txt_MatKhau.getText());
        if (rd_DangLam.isSelected()) {
            nv.setTrangThai(1);
        } else {
            nv.setTrangThai(0);
        }
        return nv;
    }
    
