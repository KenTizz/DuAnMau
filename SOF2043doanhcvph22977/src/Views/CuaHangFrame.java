    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if (cuaHangService.getOne(txt_MaCuaHang.getText()) == null) {
            String mess = cuaHangService.insert(getForm());
            loadData();
            JOptionPane.showMessageDialog(this, mess);
            clearFrom();
        } else {
            JOptionPane.showMessageDialog(this, "Trùng mã không thể add");
        }
    }//GEN-LAST:event_btn_addActionPerformed

    private void tb_DanhSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_DanhSachMouseClicked
        // TODO add your handling code here:
        int row = tb_DanhSach.getSelectedRow();
        tb_DanhSach.setRowSelectionAllowed(true);
        lbl_idCuaHang.setText(tb_DanhSach.getValueAt(row, 0).toString());
        txt_MaCuaHang.setText(tb_DanhSach.getValueAt(row, 1).toString());
        txt_TenCuaHang.setText(tb_DanhSach.getValueAt(row, 2).toString());
        txt_DiaChiCuaHang.setText(tb_DanhSach.getValueAt(row, 3).toString());
        txt_ThanhPhoCuaHang.setText(tb_DanhSach.getValueAt(row, 4).toString());
        txt_QuocGiaCuaHang.setText(tb_DanhSach.getValueAt(row, 5).toString());
    }//GEN-LAST:event_tb_DanhSachMouseClicked

    private void btn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpdateActionPerformed
        // TODO add your handling code here:
        int chon = JOptionPane.showConfirmDialog(this, "Bạn muốn update không ?", "Thông báo !", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            String mess = cuaHangService.update(lbl_idCuaHang.getText(), getForm());
            loadData();
            JOptionPane.showMessageDialog(this, mess);
            clearFrom();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn từ chối update");
        }
    }//GEN-LAST:event_btn_UpdateActionPerformed

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed
        // TODO add your handling code here:
        int chon = JOptionPane.showConfirmDialog(this, "Bạn muốn delete không ?", "Thông báo !", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            String mess = cuaHangService.delete(lbl_idCuaHang.getText());
            loadData();
            JOptionPane.showMessageDialog(this, mess);
            clearFrom();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn từ chối delete");
        }
    }//GEN-LAST:event_btn_DeleteActionPerformed

