    public void loadData() {
        tableModel = (DefaultTableModel) tb_DanhSach.getModel();
        tableModel.setRowCount(0);
        for (QLCuaHang ch : cuaHangService.getAllCuaHang()) {
            tableModel.addRow(ch.toDataRow());
        }
        
    }
    
    public QLCuaHang getForm() {
        QLCuaHang qlch = new QLCuaHang();
        qlch.setMa(txt_MaCuaHang.getText());
        qlch.setTen(txt_TenCuaHang.getText());
        qlch.setDiaChi(txt_DiaChiCuaHang.getText());
        qlch.setThanhPho(txt_ThanhPhoCuaHang.getText());
        qlch.setQuocGia(txt_QuocGiaCuaHang.getText());
        return qlch;
    }

